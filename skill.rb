class Composition
	attr_reader :name, :list, :min, :max

	def initialize(name, list, min, max)
		@name = name
		@list = list
		@min = min
		@max = max
	end

	def Composition.compositions
		@@compositions
	end

	def Composition.display(comp_i)
		s = ''
		comp = @@compositions[comp_i]
		for i in comp.min..comp.max do
			s += "#{comp.list[i]}<br>"
		end
		s
	end

